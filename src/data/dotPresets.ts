// 8x8 도트 프리셋 데이터셋 — 단일 뉴런이 의미 있게 학습할 수 있도록
// 시각적으로 또렷이 다른 도형 3쌍, 라벨당 12장씩
// '#' = 칠해진 픽셀(1), '.' = 빈 픽셀(0)

function parse(art: string[]): number[] {
  const lines = art.filter((l) => l.length === 8);
  if (lines.length !== 8) throw new Error(`pattern must be 8x8: got ${lines.length} rows: ${art.join('\n')}`);
  const pixels: number[] = [];
  for (const row of lines) {
    for (const ch of row) pixels.push(ch === '#' ? 1 : 0);
  }
  return pixels;
}

export interface PresetSet {
  labels: [string, string];
  patterns: { label: string; pixels: number[] }[];
}

